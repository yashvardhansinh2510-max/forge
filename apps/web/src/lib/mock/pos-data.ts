// ─── Helper Functions ──────────────────────────────────────────────────────────

export function getProductsByBrand(brand: string): POSProduct[] {
  return POS_PRODUCTS.filter(p => p.brand === brand && !p.isConcealed)
}

export function getProductsByCategory(brand: string, category: string): POSProduct[] {
  return POS_PRODUCTS.filter(
    p => p.brand === brand && p.category === category && !p.isConcealed
  )
}

export function getBundledParts(productId: string): POSProduct[] {
  const rules = BUNDLE_RULES.filter(r => r.mainProductId === productId)
  return rules
    .map(r => POS_PRODUCTS.find(p => p.id === r.concealedPartId))
    .filter((p): p is POSProduct => p !== undefined)
}

export function getDefaultFinish(product: POSProduct): Finish | undefined {
  return product.finishes.find(f => f.name === product.defaultFinish)
}

export function calcOfferRate(mrp: number, discountPct: number): number {
  return mrp * (1 - discountPct / 100)
}

export function calcLineTotalMRP(mrp: number, qty: number): number {
  return mrp * qty
}

export function calcLineTotalOffer(mrp: number, qty: number, discountPct: number): number {
  return calcOfferRate(mrp, discountPct) * qty
}

